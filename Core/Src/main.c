int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_CAN_Init();
  /* USER CODE BEGIN 2 */

  printf("start!\r\n");

  /* Can Start */
  if (HAL_CAN_Start(&hcan) != HAL_OK)
  {
	  Error_Handler();
  }

  TxHeader.StdId = 0x321;
  TxHeader.ExtId = 0x01;
  TxHeader.RTR = CAN_RTR_DATA;
  TxHeader.IDE = CAN_ID_STD;
  TxHeader.DLC = 8;
  TxHeader.TransmitGlobalTime = DISABLE;

  TxData[0] = 1;
  TxData[1] = 2;
  TxData[2] = 3;
  TxData[3] = 4;
  TxData[4] = 5;
  TxData[5] = 6;
  TxData[6] = 7;
  TxData[7] = 8;

//  if (HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox) != HAL_OK)
//  {
//	  printf("Can Send Fail\r\n");
//      Error_Handler();
//  }
  printf("CAN initialized\r\n");

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
	/* Check button state */
	if (HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == GPIO_PIN_SET) // Button pressed (active high)
	{
		if (buttonPressed == 0) // Check if button press is new
		{
			buttonPressed = 1; // Set button pressed flag

			/* Toggle TxData[0] */
			TxData[0] ^= 0x01; // Toggle the LSB of TxData[0]

			/* Transmit CAN message */
			if (HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox) != HAL_OK)
			{
				printf("CAN Send Fail\r\n");
				Error_Handler();
			}
			else
			{
				printf("CAN Send Success: TxData[0] = 0x%02X\r\n", TxData[0]);
			}
		}
	}
	else
	{
		buttonPressed = 0; // Reset button pressed flag when button is released
	}

	HAL_Delay(10); // Debounce delay
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

